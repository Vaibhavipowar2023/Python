'''1.Sort(): sort the list in ascending order '''
l1 = [1, 8, 2, 21, 15]
print(l1)
l1.sort()
print(l1)

'''2.reverse():reverses the list in descending order'''
l1.reverse()
print(l1)

'''3.append() :append/adds the element at the end of list'''
l1.append(45)
print(l1)

'''4.insert : insert the element  at the '''
#syntax: insert(index, element)
l1.insert(0,34) #insert 34 at index 0
print(l1)

'''5.pop() : it use to delete element of index 2 and return its value'''
l1.pop(2)
print(l1)

'''6.remove(): it remove the element from the list'''
l1.remove(21)
print(l1)


